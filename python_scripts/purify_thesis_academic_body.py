def set_paragraph_text(paragraph, text):
    if paragraph.runs:
        paragraph.runs[0].text = text
        for run in paragraph.runs[1:]:
            run.text = ""
    else:
        paragraph.add_run(text)


def set_cell_text(cell, text):
    if cell.paragraphs:
        set_paragraph_text(cell.paragraphs[0], text)
        for paragraph in cell.paragraphs[1:]:
            set_paragraph_text(paragraph, "")
    else:
        cell.text = text


def normalize_paragraphs(document):
    changed = 0
    for paragraph in document.paragraphs:
        text = paragraph.text.strip()
        if text in PARAGRAPH_REPLACEMENTS:
            set_paragraph_text(paragraph, PARAGRAPH_REPLACEMENTS[text])
            changed += 1
        elif text in TABLE_CELL_REPLACEMENTS:
            set_paragraph_text(paragraph, TABLE_CELL_REPLACEMENTS[text])
            changed += 1
    return changed


def normalize_tables(document):
    changed = 0
    for table in document.tables:
        for row in table.rows:
            for cell in row.cells:
                text = " ".join(cell.text.split())
                if text in TABLE_CELL_REPLACEMENTS:
                    set_cell_text(cell, TABLE_CELL_REPLACEMENTS[text])
                    changed += 1

    # The last table is the appendix figure index in the current document.
    appendix_table = document.tables[-1]
    for row_index, row_values in enumerate(APPENDIX_TABLE_ROWS):
        row = appendix_table.rows[row_index]
        for cell_index, value in enumerate(row_values):
            set_cell_text(row.cells[cell_index], value)
    changed += len(APPENDIX_TABLE_ROWS) * len(APPENDIX_TABLE_ROWS[0])
    return changed


